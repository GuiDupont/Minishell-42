#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	if (!ft_strchr(s, c))
		return (NULL);
	i = (int)ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == (unsigned char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
